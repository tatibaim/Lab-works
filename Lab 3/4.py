# создаём класс (шаблон объекта)
class StringHandler:

    # метод для ввода строки
    # self — это сам объект, в котором будут храниться данные
    def getString(self):
        self.s = input()   # читаем строку и сохраняем её внутрь объекта в переменную s

    # метод для печати строки
    def printString(self):
        print(self.s.upper())  # берём сохранённую строку и печатаем её в верхнем регистре


# создаём объект (экземпляр класса)
# теперь obj — реальный объект, который умеет вызывать методы класса
obj = StringHandler()

# вызываем метод ввода строки
# программа ждёт, пока пользователь введёт текст
# введённый текст сохранится в obj.s
obj.getString()

# вызываем метод печати
# берётся obj.s → переводится в верхний регистр → выводится
obj.printString()
